async function main() {
  await prisma.attemptReset.deleteMany();
  await prisma.attemptProgress.deleteMany();
  await prisma.result.deleteMany();
  await prisma.question.deleteMany();
  await prisma.section.deleteMany();
  await prisma.attestation.deleteMany();
  await prisma.user.deleteMany();

  await prisma.user.create({
    data: {
      name: 'Администратор',
      login: 'admin',
      passwordHash: await bcrypt.hash('admin123', 10),
      role: 'ADMIN',
    },
  });

  await prisma.user.create({
    data: {
      name: 'Продавец',
      login: 'seller',
      passwordHash: await bcrypt.hash('seller123', 10),
      role: 'EMPLOYEE',
    },
  });

  await prisma.attestation.create({
    data: {
      title: 'Базовая аттестация менеджера по продажам',
      passingScore: 80,
      status: 'ACTIVE',
      type: 'CONTROL',
      sections: {
        create: sections.map((section, sectionIndex) => ({
          title: section.title,
          order: sectionIndex,
          questions: {
            create: section.questions.map(([text, options, correctIndex], questionIndex) => ({
              text,
              options: JSON.stringify(options),
              correctIndex,
              order: questionIndex,
            })),
          },
        })),
      },
    },
  });
}

main().finally(() => prisma.$disconnect());
